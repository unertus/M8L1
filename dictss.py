def find_answer_in_dict_1(user_text):
    """Поиск ответа в dict_1 по ключевым словам"""
    user_text = user_text.lower().strip()
    
    for question, answer in dict_1.items():
        if question.lower() in user_text or user_text in question.lower():
            return question, answer
        for qid in quest:
            if str(qid) in user_text and quest[qid].lower() in user_text:
                return question, answer
    return None, None